package br.com.english;

/**
 *
 * @author Yuri Sanford
 */
public class Decorar {
    
    private String palavra;
    private String frase;
    private String palavra_traduzida;
    private String frase_traduzida;

    public String getPalavra() {
        return palavra;
    }

    public void setPalavra(String palavra) {
        this.palavra = palavra;
    }

    public String getFrase() {
        return frase;
    }

    public void setFrase(String frase) {
        this.frase = frase;
    }

    public String getPalavra_traduzida() {
        return palavra_traduzida;
    }

    public void setPalavra_traduzida(String palavra_traduzida) {
        this.palavra_traduzida = palavra_traduzida;
    }

    public String getFrase_traduzida() {
        return frase_traduzida;
    }

    public void setFrase_traduzida(String frase_traduzida) {
        this.frase_traduzida = frase_traduzida;
    }
    
}
